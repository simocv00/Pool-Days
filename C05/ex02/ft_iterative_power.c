int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	b;

	i = nb;
	b = power;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power == 1)
		return (nb);
	while (b > 1)
	{
		i = i * nb;
		b--;
	}
	return (i);
}
